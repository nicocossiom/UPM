package com.tapastop.app.requests;

/**
 * The type Add dish request.
 */
public class AddDishRequest {
    private String user;
    private String name;
    private String bar;
    private String origin;
    private String taste;

    /**
     * Gets user.
     *
     * @return the user
     */
    public String getUser() {
        return user;
    }

    /**
     * Sets user.
     *
     * @param user the user
     */
    public void setUser(String user) {
        this.user = user;
    }

    /**
     * Gets name.
     *
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets name.
     *
     * @param name the name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Gets bar.
     *
     * @return the bar
     */
    public String getBar() {
        return bar;
    }

    /**
     * Sets bar.
     *
     * @param bar the bar
     */
    public void setBar(String bar) {
        this.bar = bar;
    }

    /**
     * Gets origin.
     *
     * @return the origin
     */
    public String getOrigin() {
        return origin;
    }

    /**
     * Sets origin.
     *
     * @param origin the origin
     */
    public void setOrigin(String origin) {
        this.origin = origin;
    }

    /**
     * Gets taste.
     *
     * @return the taste
     */
    public String getTaste() {
        return taste;
    }

    /**
     * Sets taste.
     *
     * @param taste the taste
     */
    public void setTaste(String taste) {
        this.taste = taste;
    }
}
